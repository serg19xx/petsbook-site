#!/bin/sh

# Скрипт для коммита и пуша изменений на GitHub

# Проверка на наличие сообщения коммита
if [ -z "$1" ]; then
  echo "❌ Укажите сообщение для коммита."
  echo "Пример: ./push.sh \"Обновил документацию\""
  exit 1
fi

# Добавление всех изменений
git add .

# Коммит с сообщением
git commit -m "$1"

# Отправка в удалённый репозиторий (по умолчанию — origin main)
git push origin main

# Уведомление об успешной загрузке
echo "✅ Изменения отправлены на GitHub."